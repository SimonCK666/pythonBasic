# 有三个逻辑运算符(logical operators) ：and 、or 和not。这些运算符的含义和它们在英语的意思相似
# 例如，x > 0 and x < 10 只在x 大于0 并且小于10 时为真。
# not 运算符对一个布尔表达式取反，因此，如果x > y 为假，也就是说x 小于或等于y，则not (x > y) 为真。

# and
x > 0 and x < 10

# or
n % 2 == 0 or n % 3 == 0

# not
not(x > y)